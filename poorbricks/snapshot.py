def _canonical_rows(df: DataFrame, sort_keys: list[str] | None) -> list[dict[str, Any]]:
    """Collect rows into a JSON-serializable list with stable ordering.

    Sort by the requested keys so two runs produce byte-identical hashes
    regardless of partition order, executor count, or scheduling. Always
    append every remaining (non-key, non-array) column as a tie-breaker
    — sort_keys may not be unique (UNIQUE_KEYS describes a logical
    primary key but the table can still have duplicate keys e.g. due to
    explode), and Spark's default tie-break order is partition-dependent.
    Array columns are skipped because Spark cannot sort by them.
    """
    columns = [f.name for f in df.schema.fields]
    primary = list(sort_keys) if sort_keys else []
    # Append every other non-array column as a deterministic tie-breaker.
    array_cols = {
        f.name
        for f in df.schema.fields
        if f.dataType.typeName() in {"array", "map", "struct"}
    }
    extras = [c for c in columns if c not in primary and c not in array_cols]
    keys = primary + extras if primary else [c for c in columns if c not in array_cols]
    sorted_df = df.orderBy(*keys) if keys else df
    rows = sorted_df.collect()
    return [_row_to_jsonable({col: row[col] for col in columns}) for row in rows]


def _row_to_jsonable(row: dict[str, Any]) -> dict[str, Any]:
    return {
        k: _json_default(v) if not _is_json_native(v) else v for k, v in row.items()
    }


def _is_json_native(v: Any) -> bool:
    return v is None or isinstance(v, str | int | float | bool | list | dict)


def _json_default(value: Any) -> Any:
    if value is None:
        return None
    if isinstance(value, datetime):
        return value.replace(microsecond=0).isoformat()
    if isinstance(value, date):
        return value.isoformat()
    if isinstance(value, list):
        return [_json_default(v) if not _is_json_native(v) else v for v in value]
    if isinstance(value, dict):
        return {
            k: _json_default(v) if not _is_json_native(v) else v
            for k, v in value.items()
        }
    return str(value)


def _hash_rows(rows: list[dict[str, Any]]) -> str:
    payload = json.dumps(
        rows, sort_keys=True, separators=(",", ":"), default=_json_default
    )
    return "sha256:" + hashlib.sha256(payload.encode("utf-8")).hexdigest()


